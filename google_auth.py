#  - in the Google API console:
#      - create OAuth2 credentials
#      - download credentials as JSON
#      - save in project directory as client_secret.json
#  - run this script in a GUI desktop environment
#  - this script will prompt for authorisation in a web browser window
#  - copy the generated google-credentials.json to the Pi
